name = 'это моя строка'
print("1. Первый символ: ", name[0])
print("2. Последний символ: ", name[-1])
print("3. Подстрока с третьего по пятый символы: ", name[2:5])
print("4. Строка наоборот: ", name[::-1])
print("5. Длина строки: ", len(name))
newname = "это новая строка"
print("6. Соединенные строки: ", newname + name)